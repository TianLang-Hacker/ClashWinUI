        private static string FormatDriverVersion(uint rawVersion)
        {
            if (rawVersion == 0)
            {
                return string.Empty;
            }

            uint major = (rawVersion >> 16) & 0xFFFF;
            uint minor = rawVersion & 0xFFFF;
            return $"{major}.{minor}";
        }

        private static bool TryGetChild(YamlMappingNode mapping, string key, out YamlNode? value)
        {
            foreach (var pair in mapping.Children)
            {
                if (string.Equals((pair.Key as YamlScalarNode)?.Value, key, StringComparison.OrdinalIgnoreCase))
                {
                    value = pair.Value;
                    return true;
                }
            }

            value = null;
            return false;
        }

        private static bool GetBool(YamlMappingNode mapping, string key)
        {
            return bool.TryParse(GetString(mapping, key), out bool result) && result;
        }

        private static string GetString(YamlMappingNode mapping, string key)
        {
            if (!TryGetChild(mapping, key, out YamlNode? value))
            {
                return string.Empty;
            }

            return value switch
            {
                YamlScalarNode scalar => NormalizeScalar(scalar.Value),
                _ => string.Empty,
            };
        }

        private static bool HasValue(YamlMappingNode mapping, string key)
        {
            if (!TryGetChild(mapping, key, out YamlNode? value) || value is null)
            {
                return false;
            }

            return value switch
            {
                YamlScalarNode scalar => !string.IsNullOrWhiteSpace(scalar.Value),
                YamlSequenceNode sequence => sequence.Children.Count > 0,
                _ => true,
            };
        }

        private static string NormalizeScalar(string? value)
        {
            return value?.Trim() ?? string.Empty;
        }

        private static string AppendDetail(string message, string detail)
        {
            return string.IsNullOrWhiteSpace(detail)
                ? message
                : $"{message} {detail}";
        }

