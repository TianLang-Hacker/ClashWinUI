            return ContainsTunKeyword(name)
                || ContainsTunKeyword(description)
                || ContainsTunKeyword(extraName);
        }

        private static bool ContainsTunKeyword(string value)
        {
            string normalized = NormalizeScalar(value);
            return normalized.Contains("wintun", StringComparison.OrdinalIgnoreCase)
                || normalized.Contains("wireguard", StringComparison.OrdinalIgnoreCase)
                || normalized.Contains("mihomo", StringComparison.OrdinalIgnoreCase)
                || normalized.Contains("clash", StringComparison.OrdinalIgnoreCase);
        }

        private static uint ReadUInt32(object? value)
        {
            return value switch
            {
                uint unsigned => unsigned,
                int signed when signed > 0 => (uint)signed,
                ushort shortUnsigned => shortUnsigned,
                short shortSigned when shortSigned > 0 => (uint)shortSigned,
                long longSigned when longSigned > 0 => (uint)longSigned,
                string text when uint.TryParse(text, out uint parsed) => parsed,
                _ => 0,
            };
        }

        private static void AddAttemptedName(List<string> attemptedNames, string candidateName)
        {
            if (string.IsNullOrWhiteSpace(candidateName))
            {
                return;
            }

            if (!attemptedNames.Contains(candidateName, StringComparer.OrdinalIgnoreCase))
            {
                attemptedNames.Add(candidateName);
            }
        }

