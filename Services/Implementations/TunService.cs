        private static bool IsDnsAutoGenerated(string configPath)
        {
            try
            {
                if (string.IsNullOrWhiteSpace(configPath)
                    || !string.Equals(Path.GetFileName(configPath), "runtime.yaml", StringComparison.OrdinalIgnoreCase))
                {
                    return false;
                }

                string? directory = Path.GetDirectoryName(Path.GetFullPath(configPath));
                if (string.IsNullOrWhiteSpace(directory) || !Directory.Exists(directory))
                {
                    return false;
                }

                string sourcePath = Path.Combine(directory, "source.yaml");
                string mixinPath = Path.Combine(directory, "mixin.yaml");
                return !HasDnsSection(sourcePath) && !HasDnsSection(mixinPath);
            }
            catch
            {
                return false;
            }
        }

        private static bool HasDnsSection(string path)
        {
            if (string.IsNullOrWhiteSpace(path) || !File.Exists(path))
            {
                return false;
            }

            try
            {
                var stream = new YamlStream();
                using var reader = new StringReader(File.ReadAllText(path));
                stream.Load(reader);
                return stream.Documents.Count > 0
                    && stream.Documents[0].RootNode is YamlMappingNode root
                    && TryGetChild(root, "dns", out _);
            }
            catch
            {
                return false;
            }
        }

        private static RouteInspectionResult InspectRouteAttachment(
            TunConfigSnapshot snapshot,
            AdapterDiscoveryResult discovery,
            TunAdapterSnapshot selectedAdapter,
            ulong? adapterLuid,
            uint interfaceIndex)
        {
            bool hasAnyIdentifier = adapterLuid.HasValue || interfaceIndex != 0;
            IpHelperNative.RuntimeInspectionResult inspection = IpHelperNative.InspectRuntimeInterface(adapterLuid, interfaceIndex);
            if (!inspection.IpHelperReadSucceeded)
            {
                return new RouteInspectionResult(
                    HasIpInterface: inspection.HasIpInterface,
                    RouteAttached: inspection.RouteAttached,
                    IpHelperReadSucceeded: false,
                    IpInterfaceCheckSource: inspection.IpInterfaceCheckSource,
                    RouteTableReadSource: inspection.RouteTableReadSource,
                    InteropError: inspection.InteropError,
                    Detail: hasAnyIdentifier ? string.Empty : "No interface identifier was available for IP stack inspection.",
                    FailureMessage: string.Empty,
                    IpInterfaceEntryCount: inspection.InterfaceEntryCount,
                    RouteEntryCount: inspection.RouteEntryCount,
                    ParserVersion: inspection.ParserVersion,
                    ForeignRouteInterfaces: string.Empty,
                    HasAnyForeignTunDefaultRoute: false);
            }

            string failureMessage = string.Empty;
            string detail = hasAnyIdentifier ? string.Empty : "No interface identifier was available for IP stack inspection.";
            string foreignRouteInterfaces = string.Empty;
            bool hasAnyForeignTunDefaultRoute = false;

            if (!inspection.RouteAttached && !hasAnyIdentifier)
            {
                failureMessage = string.IsNullOrWhiteSpace(snapshot.DeviceName)
                    ? "TUN device object exists, but no interface identifier was available for IP stack inspection."
                    : $"TUN device object '{snapshot.DeviceName}' exists, but no interface identifier was available for IP stack inspection.";
            }
            else if (!inspection.RouteAttached)
            {
                (failureMessage, detail, foreignRouteInterfaces, hasAnyForeignTunDefaultRoute) = ClassifyRouteFailure(
                    snapshot,
                    discovery,
                    selectedAdapter,
                    adapterLuid,
                    interfaceIndex,
                    inspection);
            }

            return new RouteInspectionResult(
                inspection.HasIpInterface,
                inspection.RouteAttached,
                inspection.IpHelperReadSucceeded,
                inspection.IpInterfaceCheckSource,
                inspection.RouteTableReadSource,
                inspection.InteropError,
                detail,
                failureMessage,
                inspection.InterfaceEntryCount,
                inspection.RouteEntryCount,
                inspection.ParserVersion,
                foreignRouteInterfaces,
                hasAnyForeignTunDefaultRoute);
        }

        private static (string FailureMessage, string Detail, string ForeignRouteInterfaces, bool HasAnyForeignTunDefaultRoute) ClassifyRouteFailure(
            TunConfigSnapshot snapshot,
            AdapterDiscoveryResult discovery,
            TunAdapterSnapshot selectedAdapter,
            ulong? adapterLuid,
            uint interfaceIndex,
            IpHelperNative.RuntimeInspectionResult inspection)
        {
            if (!inspection.HasIpInterface)
            {
                return (
                    string.IsNullOrWhiteSpace(snapshot.DeviceName)
                        ? "TUN device object exists, but it did not appear in the IP stack."
                        : $"TUN device object '{snapshot.DeviceName}' exists, but it did not appear in the IP stack.",
                    string.Empty,
                    string.Empty,
                    false);
            }

            IReadOnlyList<TunAdapterSnapshot> foreignCandidates = GetForeignCandidateSnapshots(discovery.CandidateSnapshots, snapshot.DeviceName);
            IReadOnlyList<string> foreignRouteInterfaces = FindForeignTunDefaultRouteInterfaces(
                foreignCandidates,
                inspection.RouteSnapshots,
                adapterLuid,
                interfaceIndex);

            if (foreignRouteInterfaces.Count > 0)
            {
                string foreignInterfacesText = string.Join(", ", foreignRouteInterfaces);
                string targetName = FirstNonEmpty(snapshot.DeviceName, FirstNonEmpty(selectedAdapter.NetConnectionId, selectedAdapter.DisplayName));
                string failureMessage = string.IsNullOrWhiteSpace(targetName)
                    ? $"The target TUN interface entered the IP stack, but the default route was attached to other TUN-like interface(s): {foreignInterfacesText}."
                    : $"TUN device object '{targetName}' entered the IP stack, but the default route was attached to other TUN-like interface(s): {foreignInterfacesText}.";

                return (
                    failureMessage,
                    "Detected TUN default-route ownership outside the current profile target.",
                    foreignInterfacesText,
                    true);
            }

            string adapterName = FirstNonEmpty(snapshot.DeviceName, FirstNonEmpty(selectedAdapter.NetConnectionId, selectedAdapter.DisplayName));
            string missingRouteMessage = string.IsNullOrWhiteSpace(adapterName)
                ? "The target TUN interface entered the IP stack, but no default TUN route was attached to that interface."
                : $"TUN device object '{adapterName}' entered the IP stack, but no default TUN route was attached to that interface.";

            return (
                missingRouteMessage,
                "The target interface is present in the IP stack, but no default or split-default TUN route was found on that interface.",
                string.Empty,
                false);
        }

        private static IReadOnlyList<string> FindForeignTunDefaultRouteInterfaces(
            IReadOnlyList<TunAdapterSnapshot> foreignCandidates,
            IReadOnlyList<IpHelperNative.IpRouteSnapshot> routeSnapshots,
            ulong? targetLuid,
            uint targetInterfaceIndex)
        {
            if (foreignCandidates.Count == 0 || routeSnapshots.Count == 0)
            {
                return Array.Empty<string>();
            }

            return foreignCandidates
                .Where(candidate => HasDefaultRouteAttachment(routeSnapshots, candidate.InterfaceIndex, targetLuid, targetInterfaceIndex))
                .Select(GetAdapterConflictLabel)
                .Where(name => !string.IsNullOrWhiteSpace(name))
                .Distinct(StringComparer.OrdinalIgnoreCase)
                .ToArray();
        }

        private static bool HasDefaultRouteAttachment(
            IReadOnlyList<IpHelperNative.IpRouteSnapshot> routeSnapshots,
            uint candidateInterfaceIndex,
            ulong? targetLuid,
            uint targetInterfaceIndex)
        {
            if (candidateInterfaceIndex == 0)
            {
                return false;
            }

            bool hasIpv4Default = false;
            bool hasIpv4LowerHalf = false;
            bool hasIpv4UpperHalf = false;
            bool hasIpv6Default = false;
            bool hasIpv6LowerHalf = false;
            bool hasIpv6UpperHalf = false;

            foreach (IpHelperNative.IpRouteSnapshot routeSnapshot in routeSnapshots)
            {
                if (routeSnapshot.InterfaceIndex != candidateInterfaceIndex
                    || (targetLuid.HasValue && routeSnapshot.InterfaceLuid == targetLuid.Value)
                    || (targetInterfaceIndex != 0 && routeSnapshot.InterfaceIndex == targetInterfaceIndex))
                {
                    continue;
                }

                switch (routeSnapshot.AddressFamily)
                {
                    case IpHelperNative.AddressFamilyInet:
                        if (routeSnapshot.PrefixLength == 0 && routeSnapshot.Ipv4Address == 0)
                        {
                            hasIpv4Default = true;
                            break;
                        }

                        if (routeSnapshot.PrefixLength == 1)
                        {
                            if (routeSnapshot.Ipv4Address == 0)
                            {
                                hasIpv4LowerHalf = true;
                            }
                            else if (routeSnapshot.Ipv4Address == 0x00000080)
                            {
                                hasIpv4UpperHalf = true;
                            }
                        }

                        break;

                    case IpHelperNative.AddressFamilyInet6:
                        if (routeSnapshot.PrefixLength == 0 && routeSnapshot.Ipv6Address.IsZero)
                        {
                            hasIpv6Default = true;
                            break;
                        }

                        if (routeSnapshot.PrefixLength == 1)
                        {
                            if (routeSnapshot.Ipv6Address.IsZero)
                            {
                                hasIpv6LowerHalf = true;
                            }
                            else if (routeSnapshot.Ipv6Address.IsUpperHalfSplitDefault)
                            {
                                hasIpv6UpperHalf = true;
                            }
                        }

                        break;
                }
            }

            return hasIpv4Default
                || (hasIpv4LowerHalf && hasIpv4UpperHalf)
                || hasIpv6Default
                || (hasIpv6LowerHalf && hasIpv6UpperHalf);
        }

        private void LogRuntimeDiagnosticsIfChanged(
            TunConfigSnapshot snapshot,
            AdapterDiscoveryResult discovery,
            TunAdapterSnapshot selectedAdapter,
            ulong? adapterLuid,
            uint interfaceIndex,
            RouteInspectionResult routeInspection,
            TunRuntimeStatus runtimeStatus)
        {
            IReadOnlyList<string> foreignCandidateNames = GetForeignCandidateSnapshots(discovery.CandidateSnapshots, snapshot.DeviceName)
                .Select(GetAdapterConflictLabel)
                .Where(name => !string.IsNullOrWhiteSpace(name))
                .Distinct(StringComparer.OrdinalIgnoreCase)
                .ToArray();
            IReadOnlyList<string> thirdPartyConflictHints = GetThirdPartyTunConflictHints();

            string fingerprint = string.Join(
                " | ",
                new[]
                {
                    $"tun.device={FormatDiagnosticValue(snapshot.DeviceName)}",
                    $"attemptedOpenNames={FormatDiagnosticList(discovery.AttemptedOpenNames)}",
                    $"candidates={FormatDiagnosticCandidates(discovery.CandidateSnapshots)}",
                    $"targetAdapterPresent={discovery.TargetAdapterPresent}",
                    $"foreignCandidates={FormatDiagnosticList(foreignCandidateNames)}",
                    $"thirdPartyConflicts={FormatDiagnosticList(thirdPartyConflictHints)}",
                    $"selectedAdapter={FormatDiagnosticValue(selectedAdapter.DisplayName)}",
                    $"selectedInterfaceIndex={interfaceIndex}",
                    $"selectedLuid={(adapterLuid.HasValue ? $"0x{adapterLuid.Value:X}" : "<none>")}",
                    $"ipHelperReadSucceeded={routeInspection.IpHelperReadSucceeded}",
                    $"ipHelperParser={FormatDiagnosticValue(routeInspection.ParserVersion)}",
                    $"ipInterfaceEntryCount={routeInspection.IpInterfaceEntryCount}",
                    $"routeEntryCount={routeInspection.RouteEntryCount}",
                    $"hasIpInterface={routeInspection.HasIpInterface}",
                    $"routeAttached={routeInspection.RouteAttached}",
                    $"dnsHijackConfigured={snapshot.DnsHijackConfigured}",
                    $"dnsManaged={snapshot.DnsManaged}",
                    $"dnsAutoGenerated={snapshot.DnsAutoGenerated}",
                    $"foreignRouteInterfaces={FormatDiagnosticValue(routeInspection.ForeignRouteInterfaces)}",
                    $"ipInterfaceCheckSource={FormatDiagnosticValue(routeInspection.IpInterfaceCheckSource)}",
                    $"routeTableReadSource={FormatDiagnosticValue(routeInspection.RouteTableReadSource)}",
                    $"interopError={FormatDiagnosticValue(routeInspection.InteropError)}",
                    $"routeFailureMessage={FormatDiagnosticValue(routeInspection.FailureMessage)}",
                    $"failureKind={runtimeStatus.FailureKind}",
                    $"message={FormatDiagnosticValue(runtimeStatus.Message)}",
                });

            lock (_diagnosticGate)
            {
                if (string.Equals(_lastRuntimeDiagnosticFingerprint, fingerprint, StringComparison.Ordinal))
                {
                    return;
                }

                _lastRuntimeDiagnosticFingerprint = fingerprint;
            }

            _logService.Add($"[WARNING] TUN runtime diagnostics: {fingerprint}", LogLevel.Warning);
        }

        private void ResetRuntimeDiagnostics()
        {
            lock (_diagnosticGate)
            {
                _lastRuntimeDiagnosticFingerprint = string.Empty;
            }
        }

        private static bool LooksLikeTunAdapter(string name, string description, string extraName)
