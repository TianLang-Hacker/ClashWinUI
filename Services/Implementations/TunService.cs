                target = candidate;
            }
        }

        private static string FirstNonEmpty(string left, string right)
        {
            return !string.IsNullOrWhiteSpace(left) ? left : right;
        }

        private static string MergeDiscoverySource(string left, string right)
        {
            if (string.IsNullOrWhiteSpace(left))
            {
                return right;
            }

            if (string.IsNullOrWhiteSpace(right) || left.Contains(right, StringComparison.OrdinalIgnoreCase))
            {
                return left;
            }

            return $"{left},{right}";
        }

        private static string CombineDetails(params string[] parts)
        {
            return string.Join(
                " ",
                parts
                    .Select(NormalizeScalar)
                    .Where(part => !string.IsNullOrWhiteSpace(part)));
        }

        private static string FormatDiagnosticValue(string value)
        {
            return string.IsNullOrWhiteSpace(value) ? "<empty>" : $"'{value}'";
        }

        private static string FormatDiagnosticList(IReadOnlyList<string> values)
        {
            return values.Count == 0
                ? "[]"
                : "[" + string.Join(", ", values.Select(FormatDiagnosticValue)) + "]";
        }

        private static string FormatDiagnosticCandidates(IReadOnlyList<TunAdapterSnapshot> snapshots)
        {
            if (snapshots.Count == 0)
            {
                return "[]";
            }

            string[] items = snapshots
                .Select(snapshot =>
                    "{"
                    + $"DisplayName={FormatDiagnosticValue(snapshot.DisplayName)}, "
                    + $"NetConnectionId={FormatDiagnosticValue(snapshot.NetConnectionId)}, "
                    + $"InterfaceIndex={snapshot.InterfaceIndex}, "
                    + $"AdapterPresent={snapshot.AdapterPresent}, "
                    + $"CanOpenByWintunApi={snapshot.CanOpenByWintunApi}, "
                    + $"OpenError={FormatDiagnosticValue(snapshot.OpenError)}, "
                    + $"DiscoverySource={FormatDiagnosticValue(snapshot.DiscoverySource)}"
                    + "}")
                .ToArray();

            return "[" + string.Join("; ", items) + "]";
        }

        private static string ResolveWintunSourcePath()
        {
            string relativePath = RuntimeInformation.ProcessArchitecture switch
            {
                Architecture.X64 => Path.Combine("Tun", "x86_64", WintunFileName),
